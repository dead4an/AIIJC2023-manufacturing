    st.markdown("<div style='margin: auto; width: 150px; height: 150px; overflow: hidden;'><img src='https://github.com/gorokhovart/uts/blob/main/logo2.png?raw=true' alt='UT' style='width: 100%;'></div>", unsafe_allow_html=True)
