# Сервер и система
import streamlit as st
from streamlit.web.cli import main
import os
import gc
import tracemalloc
tracemalloc.start()

# Текст
from helpers.texts import HOME_PAGE_CONTENT

# Пути
ROOT = os.getcwd()
LOGO = os.path.join(ROOT, 'logo.png')

st.set_page_config('Home')

